#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	long int ln;

	ln = (long int)n;
	if (ln < 0)
	{
		ln *= -1;
		ft_putchar_fd('-', fd);
	}
	if (ln >= 10)
	{
		ft_putnbr_fd(ln / 10, fd);
		ft_putnbr_fd(ln % 10, fd);
	}
	else
		ft_putchar_fd(ln + '0', fd);
}
